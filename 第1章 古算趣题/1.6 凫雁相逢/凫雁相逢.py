def main():
    '''凫雁相逢'''
    d, n = 0, 0                 # 天数d，行程n
    while n < 1:                # 不小于1则为凫雁相逢
        d += 1                  # 累加凫雁飞行的天数
        n += 1 / 7 + 1 / 9      # 累加凫雁飞行的距离
    print('凫雁在第%s天相逢' % d)

if __name__ == '__main__':
    main()
