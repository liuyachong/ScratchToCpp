#include <bits/stdc++.h>
using namespace std;
// 争强斗胜 
int main()
{
	int x = 1;
	while (true) {
		int y = (36 - x) / 3; 
	    if (8 * x + 6 * y == 108) {
	    	cout << "夜叉" << x << "人，哪吒" << y << "人" << endl;
	    	break;
		}
		x += 1;
	}
    return 0;
}
